import { Cat, CatType } from "./cats.model";
import { Request, Response } from "express";

// 고양이 전체 데이터 조회 READ
export const readAllcat = (req: Request, res: Response) => {
  try {
    const cats = Cat;
    //throw new Error("db connection error");
    res.status(200).send({
      success: true,
      data: {
        cats,
      },
    });
  } catch (error) {
    res.status(400).send({
      success: false,
      error: error.message,
    });
  }
};

// 특정 고양이 데이터 조회 READ
